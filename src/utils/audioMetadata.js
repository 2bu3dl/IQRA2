// Helper function to get current word based on time
export const getCurrentWord = (currentTime, metadata) => {
  if (!metadata || !metadata.words) return null;
  
  return metadata.words.find(word => 
    currentTime >= word.startTime && currentTime <= word.endTime
  ) || null;
}; 