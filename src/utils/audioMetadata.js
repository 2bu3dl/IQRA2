// Helper function to get metadata for a specific ayah
export const getAyahMetadata = (surahNumber, ayahNumber) => {
  if (surahNumber === 1) {
    return alFatihahMetadata.ayaat[ayahNumber] || null;
  }
  if (surahNumber === 67) {
    return alMulkMetadata.ayaat[ayahNumber] || null;
  }
  return null;
};

